void main() {
  // string basic
  String firstName = 'fathil';
  String lastName = "muza";

  print(firstName);
  print(lastName);

  // string interpolitem
  var fullName = '$firstName ${lastName}';

  print(fullName);

  //karakter backslah
  var text = 'this is \'dart\'\%cool';
  print(text);

  // menghubungkan string
  var name1 = firstName + " " + lastName;
  var name2 = ("fathil" "muza");

  print(name1);
  print(name2);
}
